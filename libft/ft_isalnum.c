#include "libft.h"

int	ft_isalnum(int c)
{
	int	isdigit;
	int	isalpha;

	isdigit = (c >= '0' && c <= '9');
	isalpha = ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'));
	return ((isdigit || isalpha));
}
